from typing import List
from fastapi import FastAPI, HTTPException

app = FastAPI()


def is_valid_matrix(matrix):
    return all(len(row) == len(matrix[0]) for row in matrix)


def calculate_height_matrix(matrix):
    rows, cols = len(matrix), len(matrix[0])
    height_matrix = [[matrix[i][j] if matrix[i][j] != 0 else 0 for j in range(cols)] for i in range(rows)]

    for i in range(1, rows):
        for j in range(cols):
            if height_matrix[i][j] != 0:
                height_matrix[i][j] += height_matrix[i - 1][j]
            else:
                height_matrix[i][j] = matrix[i][j]

    return height_matrix


def calculate_area(num, height):
    return num * height


def largest_rectangle(matrix: List[List[int]]) -> tuple:
    if not matrix or not matrix[0]:
        raise HTTPException(status_code=400, detail="Input matrix is empty")

    if not is_valid_matrix(matrix):
        raise HTTPException(status_code=400, detail="Invalid matrix: Rows have different lengths")

    rows, cols = len(matrix), len(matrix[0])
    max_area = 0
    max_num = None

    height_matrix = calculate_height_matrix(matrix)

    for i in range(rows):
        for j in range(cols):
            current_num = matrix[i][j]
            current_height = height_matrix[i][j]
            current_area = calculate_area(current_num, current_height)

            for k in range(j - 1, -1, -1):
                if height_matrix[i][k] == 0:
                    break
                current_height = min(current_height, height_matrix[i][k])
                current_area = max(current_area, calculate_area(current_num, (j - k + 1) * current_height))

            if current_area > max_area:
                max_area = current_area
                max_num = current_num

    return max_num, max_area


@app.post("/largest_rectangle")
async def get_largest_rectangle(data: dict):
    try:
        matrix = data.get("matrix")
        result = largest_rectangle(matrix)
        return {"max_num": result[0], "max_area": result[1]}
    except HTTPException as e:
        return {"error": e.detail}




